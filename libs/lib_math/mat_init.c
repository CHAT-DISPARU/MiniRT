#include <lib_mat.h>

void	mat4_initial(t_mat4 *mat)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (i == j)
				mat->m[i][j] = 1.0f;
			else
				mat->m[i][j] = 0.0f;
			j++;
		}
		i++;
	}
}
