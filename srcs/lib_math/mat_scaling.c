#include <lib_mat.h>

void	mat4_scal(t_mat4 *m, t_vec3 s)
{
	mat4_initial(m);
	m->m[0][0] = s.x;
	m->m[1][1] = s.y;
	m->m[2][2] = s.z;
	m->m[3][3] = 1.0;
}
